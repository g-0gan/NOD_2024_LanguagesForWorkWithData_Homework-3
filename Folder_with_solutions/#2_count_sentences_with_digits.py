def main():
    n = int(input("Введите количество предложений: "))  # Получение количества предложений от пользователя
    count = 0  # Инициализация счетчика предложений с цифрами

    for _ in range(n):
        sentence = input("Введите предложение: ")  # Ввод предложения
        if any(char.isdigit() for char in sentence):
            count += 1  # Увеличение счетчика, если предложение содержит цифры

    # Вывод количества предложений с цифрами
    print(f"Количество предложений с цифрами: {count}")


if __name__ == "__main__":
    main()
